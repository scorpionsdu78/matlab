L =length(Y);

F = zeros(L,1);
S = zeros(L,1);
Ypred = zeros(L,1);

F(1)=1039;
S(1)=Y(2)-Y(1);

for(i=1:L)
    Y(i) = Y(i)/div;
    F(i) = F(i)/div;
    S(i) = S(i)/div;
end


prompt= 'choose alpha value: ';
alpha = input(prompt);
prompt= 'choose beta value: ';
beta = input(prompt);
prompt= 'choose thau value: ';
thau = input(prompt);

for(i=2:L)
    F(i) = alpha*Y(i) + (beta*(F(i-1)+S(i-1)));
    S(i) = beta*(F(i) - F(i-1)) + alpha * S(i-1);
end

for(i=thau+1:L)
    Ypred(i) = F(i-1) + thau*S(i-1);
end

rmse=sqrt(mean((Y(2:L)-Ypred(2:L)).^2)/L);
    
for(i=1:L)
    fprintf('%2i  | Y= %10.2f | F= %10.2f | S=  %10.2f | Ypred= %10.2f \n', i, Y(i), F(i), S(i), Ypred(i));
end

fprintf('\n\n RMSE= %10.2f \n',rmse); 

subplot(2,1,1); plot(Y);
subplot(2,1,2); plot(Ypred);